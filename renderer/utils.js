window.Hub = window.Hub || {};

Hub.showToast = function (msg, type = 'success') {
  // Safety: never show [object Object]
  let text;
  if (typeof msg === 'string') text = msg;
  else if (msg instanceof Error) text = msg.message;
  else if (msg && typeof msg === 'object') text = msg.message || msg.error || JSON.stringify(msg);
  else text = String(msg || 'Erro desconhecido');

  const container = document.getElementById('toastContainer');
  const t = document.createElement('div');
  t.className = `toast toast-${type}`;
  t.textContent = text;
  container.appendChild(t);
  setTimeout(() => {
    t.style.opacity = '0';
    t.style.transition = 'opacity 0.2s';
    setTimeout(() => t.remove(), 200);
  }, 4000);
};

Hub._confirmAction = function (message, onConfirm) {
  const backdrop = document.getElementById('modalBackdrop');
  const modal = document.getElementById('modalContent');
  modal.innerHTML = `
    <h3>Confirmar</h3>
    <div class="project-detail">
      <p style="margin:0 0 16px;color:var(--text-secondary);">${message}</p>
      <div class="modal-actions">
        <button class="btn btn-secondary" id="confirmCancel">Cancelar</button>
        <button class="btn btn-danger" id="confirmOk">Remover</button>
      </div>
    </div>
  `;
  backdrop.classList.add('visible');
  modal.querySelector('#confirmCancel').addEventListener('click', () => backdrop.classList.remove('visible'));
  modal.querySelector('#confirmOk').addEventListener('click', () => {
    backdrop.classList.remove('visible');
    onConfirm();
  });
};

Hub.fmtDur = function (s) {
  if (s === 0) return 'imagem';
  const h = Math.floor(s / 3600);
  const m = Math.floor((s % 3600) / 60);
  const sec = Math.floor(s % 60);
  if (h > 0) return `${h}:${String(m).padStart(2, '0')}:${String(sec).padStart(2, '0')}`;
  return `${m}:${String(sec).padStart(2, '0')}`;
};

Hub.fmtDate = function (iso) {
  if (!iso) return '';
  const d = new Date(iso);
  return d.toLocaleDateString('pt-PT', { day: '2-digit', month: 'short', year: 'numeric' });
};

Hub.channelDot = function (channelId) {
  const colors = { pinehat: '#8b5cf6', papertown: '#f59e0b', cortoon: '#22c55e' };
  return colors[channelId] || '#888';
};

Hub.channelName = function (channelId) {
  const ch = (Hub.state.channels || {})[channelId];
  return ch ? ch.name : channelId;
};

Hub.stateBadge = function (state) {
  const map = {
    'ideia': ['Ideia', 'badge-gray'],
    'script': ['Script', 'badge-purple'],
    'broll': ['B-Roll', 'badge-blue'],
    'edicao': ['Edição', 'badge-amber'],
    'publicado': ['Publicado', 'badge-green'],
    'rascunho': ['Rascunho', 'badge-gray'],
    'em-revisao': ['Em Revisão', 'badge-amber'],
    'finalizado': ['Finalizado', 'badge-green'],
  };
  const [label, cls] = map[state] || [state, 'badge-gray'];
  return `<span class="badge ${cls}">${label}</span>`;
};

